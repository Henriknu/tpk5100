<script lang="ts">
import Vue from "vue";
import shuffleArray from "../../service/shuffle-service";
import { Quiz } from "../../types/storeTypes";
export default Vue.extend({
  computed: {
    isMobile(): boolean {
      return this.$store.state.isMobile;
    },
    limit(): number {
      return this.$store.state.limit;
    },
  },
  methods: {
    goBack(): void {
      this.$router.push("/learnMore");
    },
    startTermsQuiz(): void {
      const questions = this.$store.getters.getTermsQuestions(7);
      shuffleArray(questions);
      const quiz = new Quiz(questions, this.limit, false, 7);
      this.$store.commit("setQuiz", quiz);
      this.$router.push("/quizInstance");
    },
    startSummaryQuiz(): void {
      const questions = this.$store.getters.getSummaryQuestions(7);
      shuffleArray(questions);
      const quiz = new Quiz(questions, this.limit, false, 7);
      this.$store.commit("setQuiz", quiz);
      this.$router.push("/quizInstance");
    },
  },
});
</script>

