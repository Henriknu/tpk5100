      <span></span>
    </div>
    <v-expansion-panels>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Terms</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <b>Success factor</b>
            <p>
              A description of ground rules (activities or elements) that should
              be adhered to during project planning and execution in order to
              achieve a successful completion of the project.
            </p>

            <b>Excessive requirements</b>
            <p>
              Demands that describe how the requirements should be implemented,
              outlining the solution. This will limit a project to examine only
              one solution (p. 68).
            </p>
            <b>Organizational complexity</b>
            <p>
              The amount of differentiation that exists within different
              elements of an organization in regards to people, activities,
              functional requirements and organizational structure. A project
              can be considered organizational complex if it requires
              contribution from multiple organizational units.
            </p>
            <b>Trust</b>
            <p>
              The extent to which one party is willing to depend on somebody, or
              something, in a given situation with a feeling of relative
              security, even though negative consequences are possible.
            </p>
            <b>Enthusiasm</b>
            <p>
              The feeling of loyalty and affiliation towards an organization,
              group or task that one is working with.
            </p>
            <b>Normative conformity</b>
            <p>
              A change in behavior or belief as a result one’s desire to fulfill
              others’ expectations and gain acceptance.
            </p>
            <b>The focusing effect</b>
            <p>
              A cognitive bias that causes us to attribute too much weight to
              events of the past and translate them into future expectations.
            </p>
            <b>Bounded rationality</b>
            <p>
              A behavioral bias that occurs when human decision-making process
              attempt to satisfice, rather than optimize.
            </p>
            <b>Structural success factor</b>
            <p>
              A success factor that describes what the people within the project
              organization and its stakeholders can do in order to achieve
              success. It may be linked with certain project characteristics.
            </p>
            <b>Soft success factor</b>
            <p>
              A success factor that describes elements regarding the culture
              within the organization and with its stakeholders that should be
              in place in order to achieve success.
            </p>
          </div>
        </v-expansion-panel-content>
      </v-expansion-panel>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Road to success</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <p>
              Empirical findings show that for projects that entail a high level
              of the following characteristics; organizational complexity,
              transformation, impact on business, constraints and uncertainty,
              there exist some critical success factors. In addition to these
              success factors that determines the rational conditions, one
              should also to create common norms and values for the organization
              (an organizational culture) and understand the impact of
              systematic biases and heuristics to avoid making these mistakes
              during the project.
            </p>
          </div>
        </v-expansion-panel-content>
      </v-expansion-panel>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Organizational complexity</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <p>
              <u>Critical Success Factors</u>
            </p>
            <ul>
              <li>
                Timely and purposeful information flow to various stakeholders
                so they know how they can contribute and to ensure that their
                interests are met.
              </li>
              <li>Clarity of roles and responsibilities.</li>
              <li>
                Project manager with adequate decision-making
                authority/appropriate project organization structure.
              </li>
              <li>Stability/continuity of project organization.</li>
            </ul>
          </div>
        </v-expansion-panel-content>
      </v-expansion-panel>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Transformation</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <p>
              If the purpose of the project is to introduce substantial changes
              to existing work processes, systems or procedures, these success
              factors are considered as critical:
            </p>
            <ul>
              <li>
                Clarity of purpose and objectives to create an understanding of
                what the change entails and why it is needed in the organization
              </li>
              <li>End-user/client/stakeholders involvement</li>
              <li>Adequate project planning</li>
              <li>
                Balanced project group that represents the interests of all
                units that will be affected by the project
              </li>
              <li>
                Project manager/management has adequate business insights, i.e.
                understands the needs and impact of various stakeholders and
                include them when necessary.
              </li>
              <li>
                Motivation for change. The individuals in the project must be
                able to see the impact they make.
              </li>
            </ul>
          </div>
        </v-expansion-panel-content>
      </v-expansion-panel>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Impact on business</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <p>
              If the project has a substantial impact on the business or
              corporate strategy, the critical success factors are:
            </p>
            <ul>
              <li>
                Common understanding of the importance of the project to the
                entire organization to create ownership and dedication to the
                objectives of the project
              </li>
              <li>
                Mobilization and provision of support from project
                owner/management
              </li>
            </ul>
          </div>
        </v-expansion-panel-content>
      </v-expansion-panel>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Constraints</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <p>
              If the project has many constraints, such as limited time and/or
              resources or other requirements from stakeholder(s), the following
              success factors are critical:
            </p>
            <ul>
              <li>
                Adequate early planning to gain insights into the workload and
                complexity of the project
              </li>
              <li>
                Routines for deviation/change control to avoid extra costs,
                conflicts and time draining activities
              </li>
              <li>
                “One team”: collaboration within project organization and with
                contractors
              </li>
              <li>Clarity of priorities and a structured requirement process</li>
            </ul>
          </div>
        </v-expansion-panel-content>
      </v-expansion-panel>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Uncertainty</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <p>
              If the project has considerable level of uncertainty regarding the
              scope of work, impact, methods or the outcome, these success
              factors are considered critical:
            </p>
            <ul>
              <li>Flexibility</li>
              <li>
                Structured risk management process where experience from
                previous projects are exploited
              </li>
              <li>
                Experienced, skilled, knowledgeable and competent project
                manager and project group
              </li>
              <li>
                Mindfulness about biases, heuristics such as overoptimism,
                narrow focus and assumptions
              </li>
            </ul>
          </div>
        </v-expansion-panel-content>
      </v-expansion-panel>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Organizational cultural factors</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <p>
              The values and norms within an organization have a huge impact on
              the organizational structural factors such as the choice of
              organizational structure, leadership type, project leader
              authority, degree of support, motivation, how and the degree of
              knowledge being shared in the organization, resistance against
              change and the hierarchy of power. In the cases in “Road to
              Success”, trust, enthusiasm, openness and respect is highlighted
              as some of the most important ones.
            </p>
          </div>
        </v-expansion-panel-content>
      </v-expansion-panel>
      <v-expansion-panel>
        <v-expansion-panel-header>
          <h5>Systematic biases</h5>
        </v-expansion-panel-header>
        <v-expansion-panel-content>
          <div>
            <p>
              Shore studied eight systematic biases and analyzed how they
              contributed to the failure of big projects. The eight systematic
              biases include:
            </p>
            <ul>
              <li>
                Accessible data: The collection of data that are easily
                accessible
              </li>
              <li>
                Conservatism: Inability to take into consideration new
                information and/or negative feedback.
              </li>
              <li>
                Dedication to a failed project: Extra resources are used to try
                to save a project that most likely will fail.
              </li>
              <li>
                Group thinking: Members of the group feel pressured to have
                similar opinions and avoid presenting evidence that might
                conflict with the general ideas of the group.
              </li>
              <li>
                Illusion of control: Decision makers are convinced that they
                have more control over the situation than they actually do.
              </li>
              <li>
                Over-optimism: High confidence concerning various aspects of the
                project that are not supported by any actual events or
                information regarding the matter.
              </li>
              <li>
                Proximity in time: place unreasonable emphasis on new
                information.
              </li>
              <li>
                Selective perception: Situation where people experience the same
                situation differently.
              </li>
              <li>
                Focus on sunk cost: Inability to accept sunk cost when
                evaluating the future benefits of a decision.
